#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	int diff;

	while (1 == 1)
	{
		diff = *s1 - *s2;
		if (diff != 0 || s1 == '\0')
			return (diff);
		s1++;
		s2++;
	}
}
